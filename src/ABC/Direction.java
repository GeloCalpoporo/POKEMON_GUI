package ABC;

/**
 * The Direction enum represents the possible movement directions in a 2D space.
 * It defines four cardinal directions: UP, DOWN, LEFT, and RIGHT.
 */
public enum Direction {
    /**
     * Represents the action of moving upward.
     */
    UP,    // Move Up

    /**
     * Represents the action of moving downward.
     */
    DOWN,  // Move Down

    /**
     * Represents the action of moving to the left.
     */
    LEFT,  // Move Left

    /**
     * Represents the action of moving to the right.
     */
    RIGHT  // Move Right
}
